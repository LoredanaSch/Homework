public class Lowercase {

    public static void main(String[] args) {

        System.out.println("This text contains lowercase: ");
        String changeLowercase =  "loredana";
        System.out.println(changeLowercase);

        String result;
        result = changeLowercase.toUpperCase();

        System.out.println("This text contains uppercase: ");
        System.out.println(result);

    }
}
